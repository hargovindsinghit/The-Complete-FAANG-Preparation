class Solution:
    def valid_sudoku(self, board):
        rows = [[False] * 9 for _ in range(9)]
        cols = [[False] * 9 for _ in range(9)]
        boxes = [[False] * 9 for _ in range(9)]

        for r in range(9):
            for c in range(9):
                if board[r][c] == ".":
                    continue
                val = int(board[r][c]) - 1
                box = (r // 3) * 3 + c // 3
                if rows[r][val] or cols[c][val] or boxes[box][val]:
                    return False
                rows[r][val] = cols[c][val] = boxes[box][val] = True
        return True
    
